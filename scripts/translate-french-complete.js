function deepMerge(target, source) {
  const output = { ...target };

  for (const key in source) {
    if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
      output[key] = deepMerge(target[key] || {}, source[key]);
    } else {
      output[key] = source[key];
    }
  }

  return output;
}

function translateValue(value, enValue) {
  // If it's already translated (different from English), keep it
  if (value !== enValue) {
    return value;
  }

  // Try to find translation
  const translation = translations[enValue];
  if (translation) {
    return translation;
  }

  // Check for common patterns
  if (typeof enValue === 'string') {
    // Handle "X days" pattern
    const daysMatch = enValue.match(/^(\d+)\s+days?$/i);
    if (daysMatch) {
      return `${daysMatch[1]} jour${parseInt(daysMatch[1]) > 1 ? 's' : ''}`;
    }

    // Handle "X hours" pattern
    const hoursMatch = enValue.match(/^(\d+)\s+hours?$/i);
    if (hoursMatch) {
      return `${hoursMatch[1]} heure${parseInt(hoursMatch[1]) > 1 ? 's' : ''}`;
    }

    // Handle "X minutes" pattern
    const minutesMatch = enValue.match(/^(\d+)\s+minutes?$/i);
    if (minutesMatch) {
      return `${minutesMatch[1]} minute${parseInt(minutesMatch[1]) > 1 ? 's' : ''}`;
    }

    // Handle "X months" pattern
    const monthsMatch = enValue.match(/^(\d+)\s+months?$/i);
    if (monthsMatch) {
      return `${monthsMatch[1]} mois`;
    }

    // Handle "X years" pattern
    const yearsMatch = enValue.match(/^(\d+)\s+years?$/i);
    if (yearsMatch) {
      return `${yearsMatch[1]} an${parseInt(yearsMatch[1]) > 1 ? 's' : ''}`;
    }

    // Handle "X weeks" pattern
    const weeksMatch = enValue.match(/^(\d+)\s+weeks?$/i);
    if (weeksMatch) {
      return `${weeksMatch[1]} semaine${parseInt(weeksMatch[1]) > 1 ? 's' : ''}`;
    }

    // Handle "Are you sure?" pattern
    if (enValue.toLowerCase().includes('are you sure')) {
      return enValue.replace(/are you sure/gi, '√ätes-vous s√ªr');
    }

    // Handle "Please" at start
    if (enValue.startsWith('Please ')) {
      return 'Veuillez ' + translateValue(value, enValue.substring(7));
    }
  }

  // Return original if no translation found
  return value;
}

function translateObject(obj, enObj, path = '') {
  const result = {};
  let translatedCount = 0;

  for (const key in obj) {
    const currentPath = path ? `${path}.${key}` : key;
    const value = obj[key];
    const enValue = enObj[key];

    if (value && typeof value === 'object' && !Array.isArray(value)) {
      const translated = translateObject(value, enValue || {}, currentPath);
      result[key] = translated.obj;
      translatedCount += translated.count;
    } else if (typeof value === 'string' && typeof enValue === 'string') {
      const translated = translateValue(value, enValue);
      result[key] = translated;

      if (translated !== value) {
        translatedCount++;
        console.log(`‚úì ${currentPath}: "${enValue}" ‚Üí "${translated}"`);
      }
    } else {
      result[key] = value;
    }
  }

  return { obj: result, count: translatedCount };
}

