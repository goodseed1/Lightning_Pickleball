// Deep merge function to preserve existing translations
function deepMerge(target, source) {
  const output = { ...target };

  for (const key in source) {
    if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
      if (target[key] && typeof target[key] === 'object') {
        output[key] = deepMerge(target[key], source[key]);
      } else {
        output[key] = source[key];
      }
    } else {
      // Only update if the current value equals the English value (untranslated)
      // OR if the key doesn't exist
      if (!target[key] || target[key] === en[key]) {
        output[key] = source[key];
      } else {
        output[key] = target[key]; // Keep existing translation
      }
    }
  }

  return output;
}

// Count untranslated keys before
function countUntranslated(en, de, path = '') {
  let count = 0;

  for (const key in en) {
    const currentPath = path ? `${path}.${key}` : key;

    if (en[key] && typeof en[key] === 'object' && !Array.isArray(en[key])) {
      count += countUntranslated(en[key], de[key] || {}, currentPath);
    } else {
      // Check if this key is untranslated (same as English or missing)
      if (!de[key] || de[key] === en[key]) {
        count++;
      }
    }
  }

  return count;
}

