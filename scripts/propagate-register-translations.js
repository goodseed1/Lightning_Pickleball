#!/usr/bin/env node
/**
 * Propagate auth.register translations from en.json to other locales
 */

const fs = require('fs');
const path = require('path');

// Locale files directory
const LOCALES_DIR = path.join(__dirname, '..', 'src', 'locales');

// Source locale (English)
const enPath = path.join(LOCALES_DIR, 'en.json');
const enData = JSON.parse(fs.readFileSync(enPath, 'utf8'));

// Extract the register section to propagate
const registerSection = enData.auth?.register;

if (!registerSection) {
  console.error('‚ùå Error: auth.register section not found in en.json');
  process.exit(1);
}

// Target locales (all except ko and en)
const targetLocales = ['de', 'es', 'fr', 'it', 'ja', 'pt', 'ru', 'zh'];

console.log('üöÄ Starting auth.register translation propagation...\n');

targetLocales.forEach(locale => {
  const filePath = path.join(LOCALES_DIR, `${locale}.json`);

  try {
    // Read the locale file
    const data = JSON.parse(fs.readFileSync(filePath, 'utf8'));

    // Ensure auth section exists
    if (!data.auth) {
      data.auth = {};
    }

    // Add or update the register section
    data.auth.register = registerSection;

    // Write back with formatting
    fs.writeFileSync(filePath, JSON.stringify(data, null, 2) + '\n', 'utf8');

    console.log(`‚úÖ Updated ${locale}.json`);
  } catch (error) {
    console.error(`‚ùå Error updating ${locale}.json:`, error.message);
  }
});

console.log('\n‚ú® auth.register translation propagation complete!');
