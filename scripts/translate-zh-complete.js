// Deep merge utility
function deepMerge(target, source) {
  const output = { ...target };

  for (const key in source) {
    if (source[key] && typeof source[key] === 'object' && !Array.isArray(source[key])) {
      output[key] = deepMerge(target[key] || {}, source[key]);
    } else {
      output[key] = source[key];
    }
  }

  return output;
}

// Find untranslated keys
function findUntranslated(enObj, zhObj, path = '') {
  const untranslated = [];

  for (const key in enObj) {
    const currentPath = path ? `${path}.${key}` : key;
    const enValue = enObj[key];
    const zhValue = zhObj[key];

    if (typeof enValue === 'object' && !Array.isArray(enValue)) {
      // Recurse into nested objects
      untranslated.push(...findUntranslated(enValue, zhValue || {}, currentPath));
    } else if (typeof enValue === 'string') {
      // Check if translation is missing or identical to English
      if (!zhValue || zhValue === enValue) {
        untranslated.push({ path: currentPath, en: enValue });
      }
    }
  }

  return untranslated;
}

