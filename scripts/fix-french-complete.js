function findUntranslated(enObj, frObj, path = '') {
  const results = [];
  for (const key in enObj) {
    const p = path ? path + '.' + key : key;
    const enVal = enObj[key];
    const frVal = frObj ? frObj[key] : undefined;
    if (typeof enVal === 'object' && enVal !== null) {
      results.push(...findUntranslated(enVal, frVal, p));
    } else if (typeof enVal === 'string' && enVal === frVal && enVal.length > 2) {
      results.push({ path: p, value: enVal });
    }
  }
  return results;
}

