// Function to create translations for a section based on English keys
function createTranslationsForSection(sectionName, enSection) {
  const translations = {};

  for (const key in enSection) {
    const enValue = enSection[key];

    if (typeof enValue === 'object' && enValue !== null) {
      // Recursively handle nested objects
      translations[key] = createTranslationsForSection(key, enValue);
    } else if (typeof enValue === 'string') {
      // Check if it's already in our frenchTranslations
      if (frenchTranslations[sectionName] && frenchTranslations[sectionName][key]) {
        translations[key] = frenchTranslations[sectionName][key];
      } else if (translate[enValue]) {
        translations[key] = translate[enValue];
      } else {
        // Use a simple translation based on common patterns
        translations[key] = translateString(enValue);
      }
    }
  }

  return translations;
}

function translateString(str) {
  // Check if exact match exists
  if (translate[str]) {
    return translate[str];
  }

  // Handle common patterns
  if (str.endsWith('...')) {
    const base = str.slice(0, -3);
    return translate[base] ? translate[base] + '...' : str;
  }

  if (str.endsWith('?')) {
    const base = str.slice(0, -1);
    return translate[base] ? translate[base] + ' ?' : str;
  }

  if (str.endsWith('!')) {
    const base = str.slice(0, -1);
    return translate[base] ? translate[base] + ' !' : str;
  }

  // Handle interpolated strings (keep them as is)
  if (str.includes('{{') || str.includes('${')) {
    return str;
  }

  // For everything else, return as is (will be caught in second pass)
  return str;
}

