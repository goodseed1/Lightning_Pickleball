// Function to translate a string
function translateString(str) {
  if (typeof str !== 'string') return str;

  // Check if already translated (contains Portuguese characters)
  if (/[áàâãéêíóôõúç]/i.test(str)) return str;

  const lowerStr = str.toLowerCase().trim();

  // Check for exact match
  if (commonTranslations[lowerStr]) {
    // Preserve original case
    if (str === str.toUpperCase()) {
      return commonTranslations[lowerStr].toUpperCase();
    }
    if (str[0] === str[0].toUpperCase()) {
      return (
        commonTranslations[lowerStr].charAt(0).toUpperCase() + commonTranslations[lowerStr].slice(1)
      );
    }
    return commonTranslations[lowerStr];
  }

  // Handle phrases with multiple words
  const words = str.split(/\s+/);
  if (words.length > 1) {
    const translated = words
      .map(word => {
        const lowerWord = word.toLowerCase();
        return commonTranslations[lowerWord] || word;
      })
      .join(' ');

    // Preserve original case of first letter
    if (str[0] === str[0].toUpperCase()) {
      return translated.charAt(0).toUpperCase() + translated.slice(1);
    }
    return translated;
  }

  return str;
}

// Recursively translate all strings in object
function translateObject(obj) {
  if (typeof obj === 'string') {
    return translateString(obj);
  }

  if (Array.isArray(obj)) {
    return obj.map(translateObject);
  }

  if (obj && typeof obj === 'object') {
    const translated = {};
    for (const key in obj) {
      translated[key] = translateObject(obj[key]);
    }
    return translated;
  }

  return obj;
}

// Find untranslated keys and translate them
function findAndTranslate(enObj, ptObj) {
  const result = { ...ptObj };

  for (const key in enObj) {
    const enValue = enObj[key];
    const ptValue = ptObj[key];

    if (typeof enValue === 'object' && !Array.isArray(enValue)) {
      result[key] = findAndTranslate(enValue, ptValue || {});
    } else if (ptValue === enValue || ptValue === undefined) {
      // This key needs translation
      result[key] = translateObject(enValue);
    }
  }

  return result;
}

