// Helper function to translate any English text to German
function translateToGerman(text) {
  if (manualTranslations[text]) {
    return manualTranslations[text];
  }
  // Return as-is if no translation found (will be caught by next iteration)
  return text;
}

// Deep merge and translate function
function deepMergeAndTranslate(target, source, enSource) {
  const output = { ...target };
  for (const key in enSource) {
    const enVal = enSource[key];
    const deVal = source ? source[key] : undefined;

    if (typeof enVal === 'object' && enVal !== null) {
      output[key] = deepMergeAndTranslate(target[key] || {}, deVal, enVal);
    } else if (typeof enVal === 'string') {
      // If German value equals English (untranslated), translate it
      if (deVal === enVal && enVal.length > 2) {
        output[key] = translateToGerman(enVal);
      } else {
        output[key] = deVal || enVal;
      }
    } else {
      output[key] = deVal !== undefined ? deVal : enVal;
    }
  }
  return output;
}

