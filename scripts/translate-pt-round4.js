function countKeys(obj) {
  let count = 0;
  for (const key in obj) {
    if (typeof obj[key] === 'object' && !Array.isArray(obj[key])) {
      count += countKeys(obj[key]);
    } else {
      count++;
    }
  }
  return count;
}

function main() {
  const localesDir = path.join(__dirname, '..', 'src', 'locales');
  const ptFile = path.join(localesDir, 'pt.json');

  console.log('âœ… Loaded existing pt.json\n');

  const ptData = JSON.parse(fs.readFileSync(ptFile, 'utf8'));
  const updatedPt = deepMerge(ptData, portugueseTranslations);

  fs.writeFileSync(ptFile, JSON.stringify(updatedPt, null, 2) + '\n', 'utf8');

  console.log('âœ… Portuguese translations updated successfully!\n');
  console.log('ðŸ“Š Translation Summary (Round 4):');

  const sections = Object.keys(portugueseTranslations).sort((a, b) => {
    const aCount = countKeys(portugueseTranslations[a]);
    const bCount = countKeys(portugueseTranslations[b]);
    return bCount - aCount;
  });

  let totalKeys = 0;
  sections.forEach(section => {
    const count = countKeys(portugueseTranslations[section]);
    totalKeys += count;
    console.log(`   - ${section}: ${count} keys`);
  });

  console.log('   ----------------------------------');
  console.log(`   TOTAL: ${totalKeys} keys translated in Round 4\n`);
}

